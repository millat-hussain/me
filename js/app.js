function currtime() {

    let date = new Date();
    let hours = date.getHours();
    let minites = date.getMinutes();
    let seconds = date.getSeconds();
    let timeformet = 'AM';


    if (hours > 12) {
        hours = hours - 12;
        timeformet = 'PM';


    }

    if (hours < 10) {
        hours = '0' + hours;
    }
    if (minites < 10) {
        minites = '0' + minites;
    }
    if (seconds < 10) {
        seconds = '0' + seconds;
    }


    let timefinal = `${hours}:${minites}:${seconds}`;


    document.getElementById('times').innerText = timefinal;
    document.getElementById('timesss').innerText = timeformet;

    setInterval(currtime, 1000);

}

